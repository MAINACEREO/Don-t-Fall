export const SAVE_KEY = 'mini_terra_pro_v1'
export function saveState(obj){ localStorage.setItem(SAVE_KEY, JSON.stringify(obj)) }
export function loadState(){ try{ return JSON.parse(localStorage.getItem(SAVE_KEY)||'null') }catch(e){ return null } }
